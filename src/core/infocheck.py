def scan_line_with_chunks(line, compiled_patterns, file_path, line_number, chunk_size=10000, overlap=1000):
    """
    分块扫描超长行（滑动窗口算法）
    
    Args:
        line: 要扫描的行
        compiled_patterns: 编译后的正则列表
        file_path: 文件路径
        line_number: 行号
        chunk_size: 每块大小（默认 10000 字符）
        overlap: 重叠区域大小（避免漏检边界数据，默认 1000 字符）
    
    Returns:
        匹配结果列表 [[matched_str, file_path, line_number], ...]
    """
    results = []
    line_len = len(line)
    start = 0
    seen_matches = set()  # 去重（重叠区域可能重复检出）
    chunk_count = 0
    
    while start < line_len:
        end = min(start + chunk_size, line_len)
        chunk = line[start:end]
        chunk_count += 1
        
        # 每 20 块输出一次进度
        if chunk_count % 20 == 0:
            progress = (end / line_len) * 100
            output_formatter.print_info(f"行 {line_number} 分块扫描进度：{progress:.1f}%（已扫描 {chunk_count} 块）")
            sys.stdout.flush()  # 强制刷新输出缓冲区
        
        # 扫描当前块
        for pattern in compiled_patterns:
            try:
                for match in pattern.finditer(chunk):
                    # 计算在原始行中的实际位置
                    actual_start = start + match.start()
                    actual_end = start + match.end()
                    matched_str = match.group()
                    
                    # 使用位置和内容组合作为去重键
                    match_key = (actual_start, actual_end, matched_str)
                    
                    if match_key not in seen_matches:
                        seen_matches.add(match_key)
                        results.append([matched_str, file_path, line_number])
            except Exception as e:
                # 单个正则出错不影响其他正则
                continue
        
        # 移动到下一块（带重叠）
        start += (chunk_size - overlap)
    
    if chunk_count > 1:
        output_formatter.print_success(f"行 {line_number} 分块扫描完成，已切换回正常扫描（共 {chunk_count} 块，检出 {len(results)} 条）")
    
    return results

